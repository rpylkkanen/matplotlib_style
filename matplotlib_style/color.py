def hex2rgb(hex_color):
    # Ensure the hex color string starts with '#'
    if hex_color[0] == '#':
        hex_color = hex_color[1:]
    # Extract RGB values
    r = int(hex_color[0:2], 16)
    g = int(hex_color[2:4], 16)
    b = int(hex_color[4:6], 16)
    return r, g, b

